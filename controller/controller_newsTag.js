var model = require("../models/newsTag.js");

var data = {

	"status" : "error",
	"detail" : "data not found"
	
};

module.exports = {

	getNewsTag : function (req,res){
		model.getNewsTag(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	},

	postNewsTag : function (req,res){
		model.postNewsTag(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	},

	putNewsTag  : function (req,res){
		model.putNewsTag(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	},

	deleteNewsTag : function (req,res){
		model.deleteNewsTag(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	}
}
