var model = require("../models/contentPhoto.js");

var data = {

	"status" : "error",
	"detail" : "data not found"
	
};

module.exports = {
	getContentPhoto : function (req,res){
		model.getContentPhoto(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	},

	postContentPhoto : function (req,res){
		model.postContentPhoto(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	},

	putContentPhoto  : function (req,res){
		model.putContentPhoto(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	},

	deleteContentPhoto :  function (req,res){
		model.deleteContentPhoto(req, function (error,result){
			if(error){
				data["status"] = "error";
				data["detail"] = error;
			}
			else{
				data["status"] = "success";
				data["detail"] = result;
			}
			res.json(data);
		});
	}
}
